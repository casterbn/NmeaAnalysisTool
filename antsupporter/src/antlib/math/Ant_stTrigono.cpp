LONG st_sin(ULONG angle)
{
    angle %= 3600;
    WORD tb_ang = (WORD)(angle % 1800);
    if (tb_ang > 900) {
        tb_ang = 1800 - tb_ang;
    }
    if (angle > 1800) {
        return -sin_table[tb_ang];
    }
    else {
        return  sin_table[tb_ang];
    }
}

LONG st_cos(ULONG angle)
{
    angle %= 3600;
    WORD tb_ang = (WORD)(angle % 1800);
    if (tb_ang > 900) {
        tb_ang = 1800 - tb_ang;
    }
    tb_ang = 900 - tb_ang;
    if ((900 < angle) && (angle < 2700)) {// minus numeric CHECK
        return -1*sin_table[tb_ang];
    } 
    else {
        return sin_table[tb_ang];
    }
}

LONG st_tan(ULONG angle)
{
    angle %= 1800;
    if (angle < 900) {
        return tan_table[angle];
    } 
    else if (angle == 900) {
        return tan_table[899];
    } 
    else {
        return -1*tan_table[1800 - angle];
    }
}

